bool timer_delay (uint32_t ms) // ms - milisekunde
{
  static uint32_t start ; // Sačuvano vreme ulaska u funkciju.
  static bool flag = true;

  if (flag)
  {
	  start = sys_time;
	  flag = false;
  }

  if ((start + ms) > sys_time)
    {
      return false;
    }

  flag = true;

  return true;
}


